export function getPrompt(type: keyof typeof PROMPTS, params: Record<string, string>): string {
  let prompt = PROMPTS[type];
  for (const [key, value] of Object.entries(params)) {
    prompt = prompt.replace(new RegExp(`\\{${key}\\}`, 'g'), value);
  }
  return prompt;
}
